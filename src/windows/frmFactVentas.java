    private void quitarItem() {
          int fsel = tablafactvent.getSelectedRow();  
          if(fsel>=0){
              String xcod = tablafactvent.getValueAt(fsel, 0).toString();
              String xn = tablafactvent.getValueAt(fsel, 1).toString();
              int resp = JOptionPane.showConfirmDialog(this, "Confirma que desea eliminar el item  *"+xn.toUpperCase()+"* del Sistema?","Confirme...",
                    JOptionPane.YES_NO_OPTION);
              if(resp == JOptionPane.YES_OPTION){
                sentencia = "DELETE FROM factventa_det WHERE idproductos='"+xcod+"'";
          }
          }try {
            ejecutar.executeUpdate(sentencia);
        } catch (SQLException ex) {
            Logger.getLogger(frmFactVentas.class.getName()).log(Level.SEVERE, null, ex);
        }
          mostrarDetalle();
          limpiarCuadros();
    }

