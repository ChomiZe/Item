 private void inicio() {
        try {
            ejecutar = conex.getConexion().createStatement();
            mostrarFuncionarios();
           modoEdicion(false);
        } catch (SQLException ex) {
            Logger.getLogger(frmFunc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void mostrarFuncionarios() {
       tabla.setRowCount(0);
        tabla = (DefaultTableModel)tablaFuncionarios.getModel();
        sentencia = "SELECT * FROM funcionarios";
        try {
            resultado = ejecutar.executeQuery(sentencia);
            while (resultado.next()){
                Object fila[] = new Object[5];
                fila[0] = resultado.getInt(1);
                fila[1] = resultado.getString(2);
                fila[2] = resultado.getInt(3);
                fila[3] = resultado.getString(4);
                fila[4] = resultado.getInt(5);
                tabla.addRow(fila);
            }
            tablaFuncionarios.setModel(tabla);
        } catch (SQLException ex) {
            Logger.getLogger(frmFunc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

