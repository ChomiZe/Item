    private void MostrarTipocto() {
        tabla.setRowCount(0);
        tabla = (DefaultTableModel)tablacto.getModel();
        sentencia = "SELECT * FROM tipo_cto";
        try {
            resultado = ejecutar.executeQuery(sentencia);
            while(resultado.next()){
                Object fila[] = new Object[2];
                fila[0] = resultado.getInt(1);
                fila[1] = resultado.getString(2);
                
                tabla.addRow(fila);
            }

        } catch (SQLException ex) {
            Logger.getLogger(frmTipocto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

