    private void accionAgregar() {
         sentencia = "SELECT MAX(idtipo_cto) FROM tipo_cto";
        try {
            resultado = ejecutar.executeQuery(sentencia);
             resultado.first();
            limpiarCuadros();
            txtID.setText(Integer.toString(resultado.getInt(1)+1));
            modoEdicion(true);
            operacion = 'A';
            txtdescrip.grabFocus();
        } catch (SQLException ex) {
            Logger.getLogger(frmTipocto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void limpiarCuadros() {
    txtID.setText(null);
        txtdescrip.setText(null);
    }

    private void accionGuardar() {
        String v1 = txtID.getText();
        String v2 = txtdescrip.getText();
        if (v2.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe"+ " ingresar datos"," Revise el casillero vacio",JOptionPane.ERROR_MESSAGE);
            txtdescrip.grabFocus(); 
         }
        sentencia = "INSERT INTO tipo_cto VALUES('"+v1+"','"+v2+"')";
         if (operacion == 'M'){
            sentencia = "UPDATE tipo_cto SET desscripCTO='"+v2+"'WHERE idtipo_cto='"+v1+"'";
        }
        try {
            ejecutar.executeUpdate(sentencia);
            limpiarCuadros();
        } catch (SQLException ex) {
            //Logger.getLogger(frmMarcas.class.getName()).log(Level.SEVERE, null, ex);
        }
        MostrarTipocto();
        modoEdicion(false);
    }

    private void accionCancelar() {
        modoEdicion(false);
    }

    private void accionModificar() {
         if(tablacto.getSelectedRow()>=0){
            operacion = 'M';
            modoEdicion(true);
            txtdescrip.grabFocus();
       }else{
            JOptionPane.showMessageDialog(this,"No se ha seleccionado ningun registro","Seleccione...",JOptionPane.ERROR_MESSAGE);
        }
    }

    private void accionEliminar() {
     int fsel = tablacto.getSelectedRow();
        if(fsel>=0){
             String xid = tablacto.getValueAt(fsel, 0).toString();
             String xnom = tablacto.getValueAt(fsel,1).toString();
             int resp = JOptionPane.showConfirmDialog(this, "Confirma que desea eliminar el contacto *"+xnom.toUpperCase()+"* del Sistema?","Confirme...",
                    JOptionPane.YES_NO_OPTION);
              if(resp == JOptionPane.YES_OPTION){
                sentencia = "DELETE FROM tipo_cto WHERE idtipo_cto='"+xid+"'";
                try {
                    ejecutar.executeUpdate(sentencia);
                } catch (SQLException ex) {
                    Logger.getLogger(frmTipocto.class.getName()).log(Level.SEVERE, null, ex);
                }
        MostrarTipocto(); 
              }
    }
}

    private void buscarCto() {
         String criterio = JOptionPane.showInputDialog("Ingrese nombre");
        sentencia = "SELECT * FROM tipo_cto WHERE desscrip_cto LIKE'%"+criterio+"%'";
        
        tabla.setRowCount(0);
        tabla = (DefaultTableModel) tablacto.getModel();
        try {
            resultado = ejecutar.executeQuery(sentencia);
           while(resultado.next()){
                Object fila[] = new Object[2];
                fila[0] = resultado.getInt(1);
                fila[1] = resultado.getString(2);
                
                tabla.addRow(fila);
           }
        } catch (SQLException ex) {
            Logger.getLogger(frmTipocto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void cargarCuadros() {
        int fsel = tablacto.getSelectedRow();
        if(fsel>=0){
             String xid = tablacto.getValueAt(fsel, 0).toString();
              sentencia = "SELECT * FROM tipo_cto WHERE idtipo_cto = '"+xid+"'";
            try {
                resultado = ejecutar.executeQuery(sentencia);
                 resultado.first();
                txtID.setText(resultado.getString(1));
                txtdescrip.setText(resultado.getString(2));
            } catch (SQLException ex) {
                Logger.getLogger(frmTipocto.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    }
