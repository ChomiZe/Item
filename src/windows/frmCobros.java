    private void mostrarCobros() {
        tabla.setRowCount(0);
        tabla = (DefaultTableModel) tablaCobros.getModel();
        sentencia = "SELECT * FROM vistacobro";
        try {
            resultado = ejecutar.executeQuery(sentencia);
            while (resultado.next()){
                Object fila[]= new Object[8];
                fila[0] = resultado.getInt(1);
                fila[1] = resultado.getInt(2);
                fila[2] = resultado.getString(3);
                fila[3] = resultado.getInt(8);
                fila[4] = resultado.getInt(4);
                fila[5] = resultado.getDouble(5);
                  fila[6] = resultado.getString(6);
                   fila[7] = resultado.getString(7);
                
                
                tabla.addRow(fila);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmCobros.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void cargarCuadros() {
        int fsel = tablaCobros.getSelectedRow();
        if(fsel >=0){
            String xnro = tablaCobros.getValueAt(fsel, 0).toString();
            sentencia = "SELECT * FROM vistacobro WHERE idcobro_cuotas = '"+xnro+"'";
            try {
                resultado = ejecutar.executeQuery(sentencia);
                resultado.first();
                txtnro.setText(resultado.getString(1));
                txtnrofact.setText(resultado.getString(4));
                txtclie.setText(resultado.getString(2));
                txtraz.setText(resultado.getString(3));
                txtru.setText(resultado.getString(8));
              
                txtentrega.setText(resultado.getString(5));

            } catch (SQLException ex) {
                //Logger.getLogger(frmCobros.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void accionAgregar() {
        sentencia = "SELECT MAX(idcobro_cuotas) FROM cobro_cuotas";
        try {
            resultado = ejecutar.executeQuery(sentencia);
            resultado.first();
            limpiarCuadros();
            txtnro.setText(Integer.toString(resultado.getInt(1) + 1));
            modoEdicion(true);
            operacion = 'A';
            txtnrofact.grabFocus();
        } catch (SQLException ex) {
            Logger.getLogger(frmCobros.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

